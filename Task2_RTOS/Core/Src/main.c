void vUartTask( void * pvParameters )
{
    /* The parameter value is expected to be 1 as 1 is passed in the
    pvParameters value in the call to xTaskCreate() below. */
    configASSERT( ( ( uint32_t ) pvParameters ) == 1 );
		TickType_t xLastWakeTime;
		const TickType_t xFrequency = 50;
    // Initialise the xLastWakeTime variable with the current time.
    xLastWakeTime = xTaskGetTickCount();
	  
		char suspensionState[15];
		int i = 0;
    for( ;; )
    {
				if(xSemaphoreTake(xUartSemaphore, portMAX_DELAY))
				{
					/* Task code goes here. */
						for(i = 0; i < 4; i++)
						{
							convertedValues[i] = (float) adcValues[i] - 2048.0;
							convertedValues[i] = (convertedValues[i] * 5) / 2048;
							if(convertedValues[i] > 0 )
								strcpy(suspensionState, "Compressed \r\n");
							else if (convertedValues[i] < 0)
								strcpy(suspensionState, "Expanded \r\n");
							else
								strcpy(suspensionState, "\r\n");
							snprintf(txUartBuffer, 25, "val %d = %.2f cm ", i+1 , (float) convertedValues[i]);
						  HAL_UART_Transmit(&huart1, (uint8_t*) txUartBuffer,strlen(txUartBuffer), HAL_MAX_DELAY);
							HAL_UART_Transmit(&huart1, (uint8_t*) suspensionState,strlen(suspensionState), HAL_MAX_DELAY);
						}
				}
				
    }
		vTaskDelayUntil(&xLastWakeTime, xFrequency);
}

void vCanTask( void * pvParameters )
{
    /* The parameter value is expected to be 1 as 1 is passed in the
    pvParameters value in the call to xTaskCreate() below. */
    configASSERT( ( ( uint32_t ) pvParameters ) == 1 );
		TickType_t xLastWakeTime;
		const TickType_t xFrequency = 50;
     // Initialise the xLastWakeTime variable with the current time.
     xLastWakeTime = xTaskGetTickCount();
		int i = 1;

    for( ;; )
    {
        /* Task code goes here. */
			if(xSemaphoreTake(xCanSemaphore, portMAX_DELAY))
			{
				
				snprintf(txCanBuffer, 6, "%.2f", (float) convertedValues[i]);
				HAL_CAN_AddTxMessage(&hcan, &TxHeader, (uint8_t *)txCanBuffer ,&mailBox);
			}
			vTaskDelayUntil(&xLastWakeTime, xFrequency);
    }
}

